    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        //Adicionando Data e HORA
        Date data = new Date();
        SimpleDateFormat formatar = new SimpleDateFormat("dd/MM  H:mm");
        String dataFormatada = formatar.format(data);

        //Adicionando todos os campos para resultado;
        int cod_produto = Integer.parseInt(
                TextNumPedido.getText());
        String vendedor = (TextVendedor.getText());
        int quant = Integer.parseInt(TextQuantidade.getText());

        double taxa = 0;

        if (txtPizzaG.isSelected()) {//pizza grande
            taxa = 30;
        } else if (txtPizzaM.isSelected()) {//pizza média
            taxa = 20;
        } else if (txtPizzaP.isSelected()) {//pizza pequena
            taxa = 10;
        }
        //tela de taxa da pizza selecionada;
        TextPrecoPizza.setText(
                "" + taxa);

        double retirada = 0;

        //Condições para os tipos de coberturas;
        if (CheckCalabresa.isSelected()) { //Calabresa
            retirada = calabresa;
            /*if (CheckQueijo.isSelected()){    Para seleção de 2 recheios
                retirada = retirada + calabresa/2;
            } */
        }
        if (CheckQueijo.isSelected()) { //4Queijos
            retirada = quatroQueijos;
            /* if(CheckBacon.isSelected()){
                retirada = retirada + (bacon - 10);
            }*/
        }
        if (CheckBacon.isSelected()) { //Bacon
            retirada = bacon;
        }
        TextPrecoRecheio.setText("" + retirada);

        double taxaDelivery = 7;

        //Total calculado;
        double total_pagar = (taxa * quant) + retirada;

        //Adicionando ao arquivo .txt
        Cad_info cad_info = new Cad_info();
        //Textos
        cad_info.setNum_pedido(TextNumPedido.getText());
        cad_info.setVendedor(TextVendedor.getText());
        cad_info.setQuant(TextQuantidade.getText());
        cad_info.setData_pedido(dataFormatada);
        
        //Delivery
        if (txtEntrega.isSelected()) {
            total_pagar = total_pagar + taxaDelivery;
            cad_info.setDelivery(txtEntrega.getText());
            TextPrecoTotal.setText("" + total_pagar);

        } else {
            cad_info.setDelivery(txtLocal.getText());
            TextPrecoTotal.setText("" + total_pagar);
        }

        cad_info.setPreco_total(TextPrecoTotal.getText());

        //Tela com mais detalhes>>
        jTextArea1.setText("Olá, aqui está o ID do seu pedido: " + cod_produto
                + "\n=========================================================="
                + "\nVendedor: " + vendedor
                + "\t\t Data e Hora: " + dataFormatada
                + "\n=========================================================="
                + "\n MAIS INFORMAÇÕES NA COMANDA IMPRESSA"
                + "\n=========================================================="
                + "\nTotal: " + total_pagar);

        if (txtPizzaG.isSelected()) {
            cad_info.setTam_pizza(txtPizzaG.getActionCommand());
        }
        if (txtPizzaM.isSelected()) {
            cad_info.setTam_pizza(txtPizzaM.getActionCommand());
        }
        if (txtPizzaP.isSelected()) {
            cad_info.setTam_pizza(txtPizzaP.getActionCommand());
        }

        //Recheios
        if (CheckCalabresa.isSelected()) {
            cad_info.setRecheio_pizza(CheckCalabresa.getText());
        }
        if (CheckQueijo.isSelected()) {
            cad_info.setRecheio_pizza(CheckQueijo.getText());
        }
        if (CheckBacon.isSelected()) {
            cad_info.setRecheio_pizza(CheckBacon.getText());
        }
        JOptionPane.showMessageDialog(null, cad_info.Salvar());

        CadastroComanda_sql();


    }//GEN-LAST:event_jButton2ActionPerformed

