    public void CadastroComanda_sql() {
        //Aqui ele irá fazer o comando ao mysql
        String sql = "insert into main values (?,?,?,?,?,?,?)";

        try {
            //as linhas a seguir preparam a consulta ao BD para o login
            // o ? será substituido pelo que for escrito nos campos de texto de login e senha
            pst = conexao2.prepareStatement(sql);
            pst.setString(1, TextNumPedido.getText());
            pst.setString(2, TextVendedor.getText());
            //Estrutura de decisão tamanho
            if (txtPizzaG.isSelected()) {
                pst.setString(3, txtPizzaG.getActionCommand());
            }
            if (txtPizzaM.isSelected()) {
                pst.setString(3, txtPizzaM.getActionCommand());
            }
            if (txtPizzaP.isSelected()) {
                pst.setString(3, txtPizzaP.getActionCommand());
            }
            //Estrutura de decisão de recheio
            if (CheckQueijo.isSelected()) {
                pst.setString(4, CheckQueijo.getText());
            }
            if (CheckCalabresa.isSelected()) {
                pst.setString(4, CheckCalabresa.getText());
            }
            if (CheckBacon.isSelected()) {
                pst.setString(4, CheckBacon.getText());
            }
            pst.setString(5, TextQuantidade.getText());
            //Adiciona data e hora padrão ao mysql
            Date data = new Date();
            SimpleDateFormat formatar = new SimpleDateFormat("YYYY-MM-dd hh:mm:ss");
            String dataFormatada = formatar.format(data);
            pst.setString(6, dataFormatada);
            //Estrutura de decisão pra o delivery
            if (txtEntrega.isSelected()) {
                pst.setString(7, txtEntrega.getText());
            }
            if (txtLocal.isSelected()) {
                pst.setString(7, txtLocal.getText());
            }
            //A seguir iremos consultar a Query do SQL
            pst.executeUpdate();

        } catch (SQLException e) { //Exibe o erro "posso por apenas o e que informa detalhadamente"
            JOptionPane.showMessageDialog(null, e);
        }
    }

